# Fungsi ini digunakan untuk menampilkan image docker yang telah dibuat
docker image ls

# Fungsi ini digunakan untuk menyimpan image docker yang telah dibuat (Contohnya image redis dengan version latest)
docker image pull redis:latest

# Fungsi ini digunakan untuk menghapus image redis yang telah dibuat (Contohnya disini menghapus image redis dengan version latest)
docker image rm redis:latest