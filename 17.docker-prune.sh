# Fungsi ini digunakan untuk menghapus semua container yang sudah di nonaktifkan
docker container prune

# Fungsi ini digunakan untuk menghapus semua image yang sudah tidak digunakan
docker image prune

# Fungsi ini digunakan untuk menghapus semua volume yang sudah tidak digunakan
docker volume prune

# Fungsi ini digunakan untuk menghapus semua network yang sudah tidak digunakan
docker network prune

# Fungsi ini digunakan untuk menghapus semua container, image, volume, dan network yang sudah tidak digunakan
docker system prune